     * Set cSecret
     *
     * @param string $cSecret
     *
     * @return OauthConsumer
     */
    public function setCSecret($cSecret)
    {
        $this->cSecret = $cSecret;

        return $this;
    }

    /**
     * Get cSecret
     *
     * @return string
     */
    public function getCSecret()
    {
        return $this->cSecret;
    }
}

